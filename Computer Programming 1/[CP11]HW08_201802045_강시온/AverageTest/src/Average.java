public class Average {
	public int getAverage(int a, int b) {			//2개 인수의 평균
		return (a + b) / 2;							//a와 b의 평균
	}
	public int getAverage(int a, int b, int c) {	//3개 인수의 평균
		return (a + b + c) / 3;						//a,b,c의 평균
	}
}
